	    <div class="title">Wtf is Art of War?</div>

      <div class="metadata">April 10, 2015 by Jake Galletta </div>

      <div class="body">

        <p>Art of War is a turn based strategy game i have been developing for the past couple months based on an over 2,000 year old ancient Chinese military document titled, The Art of War.
	        Being facinated by history my whole life. I wanted to make a game that puts the player in a position of power during a specific time in history.
		      With my recent big interest in english history, ive given control to the player over a small bronze age celtic tribe in England. Crucial economic and political decisions as well
		        as scientific/techological advancement choices will be made that shape your rule, whether you focus more on military, economics, or agriculture. The player will attempt to grow
		          their kingdom and survive history past the bronze age and 3,000 years of invasions from the Romans, the Vikings and the Normans. However military strength wont be enough to survive
		            past history, your ability to analyize, learn, handle diplomatic relations, tactics and understanding of historical effects will carry you past the bronze age into the iron age.
		              This isnt a strategy game just about military unit consequences, its about how technological, diplomatic, and economic consequences effect each other. For example, having a big standing army
		                would consume extra gold and food resources that would force the player to build more farms or try to yeild more food to either sell for gold or feed his peasants. Also having too big of an army would employ too
                    many peasants into military service which will not produce resources like a farmer or miner peasant would. This game will be a historical toy box with one difficulty setting of managing and growing a small celtic tribe into a formitable kingdom.</p>



