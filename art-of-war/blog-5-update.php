 <div class="title">Summer Time and Java Runtime - End of Summer Update

     	 <div class="metadata">September 25, 2015 by Jake Galletta </div>

     	 <div class="body">
<p>In a caffienated stupor chased with hash and episodes of Community the summer comes to a close, Im lucky i remember to update 
this atleast once a month..man Communitys a good show. Anyway, Septembers been a busy month so far, editing 2,3 trailers, a failed greenlight campaign and art
assets overhauls. It has been a very exciting experience to put something out there on the internet and recieve feedback. 
I didnt want to put it up on steam till i knew the game ran on all 3 platforms(Win,Mac,&Linux). 
But sometimes you develop on one computer for so long when you run it on another device it fails.
 Javas write once and compile everywhere seems to not be so. The problem turned out the game was exceeding Javas default heap size,
 and as a result i had to use the ProcessBuilder class to relaunch the driver class and override the JVM environment setting.
 Which sadly took 2 days to fix, i wont make that mistake again.
 After that i played around with video encoder settings in OBS for way too long than i would have liked.
 Im thinking of going back to fraps since it makes my gifs look nice and doesnt distort and saturate my image like OBS does.
I got my trailer my 3 screenshots, my greenlight liscence and i was ready. Apparently after 6,7 days i wasnt...my
 yes/no vote ratio out of 300 votes was 1:3 which wasnt that great, but the fact i got any votes is pretty good none the less.
I would have posted a screenshot of the stats if i have remembered.

<p>

<p>
As a take away from my greenlight experience, Take your time and make sure your game is complete & polished enough to convey its uniqueness in the trailer. 
YOUR GAME WILL BE JUDGED ON ITS APPEARENCE, soo you will have one chance to make a good first impression for that yes vote. Do not show large ui elements or early placeholder/programmer art. Also make sure your trailer contains atleast 30 secs of gameplay. I made the mistake of a 
minute trailer w/ 12,15 secs of gameplay and had a bit of comments demanding more gameplay. Which made me crunch 2 days to produce another
video with pure gameplay that showed off the first 5 minutes of gameplay. So now im redefingin my art style and
overhauling art assets. Personally till the game is fully out of the design and planning stage and in a more good demo state i will restart my campaign. Look for Art of War: Mediolanum in GreenLight Concepts.
