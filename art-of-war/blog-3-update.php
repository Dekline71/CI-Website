	    <h1>Now where was I ?</h1>

	     <p>I wanna continue where i last left off in the last post on how damage is dealt in a.o.w. I previously mentioned how each different unit has a unique set of attack and defense points depending on what class of armor or weapon they use. For example leather armor will have a lower armor rating than chain-mail armor and leather units will be naturally cheaper to produce since mining is not involved to produce leather. An example for weapons would be a bronze weapon would be much cheaper to produce and deal less damage than a steel or iron weapon. This will create a strength and weakness in each different type of unit and a military/economic consequence in the mining or farming of those resources needed to produce. Before i end this post i want to introduce another mechanic that will greatly influence how damage is dealt, positioning. Positioning is the action or strategy the player will execute in which direction their units should face. If a unit is directly facing another enemy unit, normal damage will be applied, but if a unit is facing directly behind another unit, 2x additional damage is applied because from behind the unit is unprotected and unaware of attack from that direction. Also if an attack is engaged to the left or right side of an opponent, 1.5x the additional damage is applied. </p>

