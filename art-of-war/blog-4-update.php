	 <p>
		 Between new episodes of Mr. Robot, John Oliver,
     <br>the Jimquisition and wearing the many hats the development of this game requires, i havent had a good moment to update the blog but im updating the progress more fequently on twitter @consciousint and now the website.
		 But Wow i cannot believe its already half way through August, the time really flys when your in the thick of development. I have a alot to write about but ill try to keep it brief.
		 Art of War, the turn based strategy game based on Prehistoric and Iron age England i been working on for the past year is close to greenlight. I spent about the last month and a half
     painting different revisions of the main battle/engagement map. From an oil style to a ink style back to a better oil art style. A very laborious process but im very happy with the
     overall result. Without all the trial and error i would not have gotten the defining look of the map. Im refining it as development continues as most of Scotland and Ireland are missing at the moment
     with mountain, hills and some major forests. Also i have been working on the art for units, that have improved from a flat perspective to a over the should angle from above with a bit more detail. Below i will post some before and after images of the map and unit improvement.
